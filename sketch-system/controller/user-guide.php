<h3>Add Page  </h3>
<p>Click on the ADD PAGE tab to open the add page window. </p>
<p>From here you can create a single page,
  or multiple pages at once. A single page will display several options and
  allows for a more detailed setup, while creating multiple pages will create a
  site structure and is useful when starting a new site or a new section to your
  website.</p>
<h3>Create a Single Page</h3>
<h4>Menu Name:</h4>
<p>Enter the name that you'd
  like to be displayed on your website navigation for this page. </p>
<p class="notice">Note: Please make this name
  unique as no two pages can share the same name on the website.</p>
<h4>Page Under:</h4>
<p>This allows you to
  specify where on your website you would like this page to reside. You may want
  a news update to be a page under news. </p>
<h4>Page Type:</h4>
<p>Depending
  on your website you may have special types of pages, eg. general web page,
  news, members. Most of the time the 'general' will be the option needed as this
  is used for most web pages.</p>
<h4>Publish Status:</h4>
<p>There are three options available here, 'publish', 'hidden' and 'members
  only'.</p>
<ul>
  <li>Publish makes the page
    available on the website.</li>
  <li>Hidden makes the page only
    viewable when you're logged to sketch.</li>
  <li>Members only would be selected
    for a page that you are adding that goes behind a login.</li>
</ul>
<h4>On Menu:</h4>
<p>Select
  Yes or No to add this page to the website navigation.</p>
<h4>Template:</h4>
<p>Select
  the template that best suits the page. </p>
<p>If the page is a news or case studies page, there may be a special template
  available to <br>
  make the page display content correctly.</p>
<h4>Page Form:</h4>
<p>If
  your page has special or custom input requirements you may need to specify a
  page form, otherwise please select the default option 'NONE (standard)'.</p>
<h4>Page Output Template:</h4>
<p>If your page has a custom layout then you may need to specify an
  output template, otherwise please select the default option 'NONE
  (standard)'.</p>
<h4>Adding Multiple Pages:</h4>
<p>Multiple pages allow sub pages to be
  created, these pages will be indented to indicate they are sub pages. </p>
